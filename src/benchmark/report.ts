import type { BenchmarkSummary } from './capture';

function fmt(n: number, decimals = 1): string {
  return n.toFixed(decimals);
}

function verdictBand(avgFps: number, p1Fps: number): string {
  if (avgFps >= 60 && p1Fps >= 45) return 'Stay R3F — full speed ahead';
  if (avgFps >= 45 && p1Fps >= 30) return 'Stay R3F — budget carefully per-biome';
  if (avgFps >= 30) return 'Marginal — port to Godot likely';
  return 'Port to Godot — bet the farm';
}

/**
 * Build a markdown report from a BenchmarkSummary.
 * Includes summary table + mermaid gantt for frame-time distribution.
 */
export function buildMarkdownReport(summary: BenchmarkSummary): string {
  const verdict = verdictBand(summary.avgFps, summary.p1Fps);

  const buckets = Array.from({ length: 10 }, (_, i) => {
    const lo = i * 6;
    const hi = lo + 6;
    const count = summary.frames.filter(
      (f) => f.frameTimeMs >= lo && f.frameTimeMs < hi,
    ).length;
    return { lo, hi, count };
  });

  const maxCount = Math.max(...buckets.map((b) => b.count), 1);

  const ganttRows = buckets
    .map((b) => {
      const pct = Math.round((b.count / summary.frameCount) * 100);
      const bar = '█'.repeat(Math.round((b.count / maxCount) * 20));
      return `  ${String(b.lo).padStart(2)}–${String(b.hi).padStart(2)}ms : ${bar} ${pct}%`;
    })
    .join('\n');

  return [
    `# Benchmark Report — ${summary.route}`,
    '',
    `**Date:** ${summary.startedAt.slice(0, 10)}  `,
    `**Device:** ${summary.device}`,
    '',
    '## Summary',
    '',
    '| Metric | Value |',
    '|---|---|',
    `| Avg FPS | **${fmt(summary.avgFps)}** |`,
    `| p1 FPS | **${fmt(summary.p1Fps)}** |`,
    `| p5 FPS | ${fmt(summary.p5Fps)} |`,
    `| Peak frame time | ${fmt(summary.peakFrameTimeMs)} ms |`,
    `| Avg frame time | ${fmt(summary.avgFrameTimeMs)} ms |`,
    `| Peak draw calls | ${summary.peakDrawCalls} |`,
    `| Peak triangles | ${summary.peakTriangles.toLocaleString()} |`,
    `| Peak JS heap | ${fmt(summary.peakJsHeapMb)} MB |`,
    `| Frame count | ${summary.frameCount} |`,
    `| Duration | ${fmt(summary.durationMs / 1000)} s |`,
    '',
    '## Verdict',
    '',
    `**${verdict}**`,
    '',
    '## Frame-time distribution',
    '',
    '```',
    ganttRows,
    '```',
    '',
    `*Generated by kings-road benchmark harness — ${summary.userAgent}*`,
    '',
  ].join('\n');
}
